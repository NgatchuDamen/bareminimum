    <h1>Preface to the Cameroon Hymnal</h1>

    <p align="justify" style="line-height: 100%; margin-bottom: 0in">
 <i>	The Fathers  of the Second Vatican Council decreed that “
 Religious singing by the  people is to be skillfully fostered, so
 that in devotions and sacred exercises, as also during liturgical
 services, the voices of the faithful may ring out according to the
 norms and requirements of the rubrics” (<span style="font-variant: small-caps">Sacrosanctum
 Concilium,</span> n. 118). This new Cameroon Hymnal is welcome
 because it will foster the active participation of the entire
 assembly of the faithful at Holy Mass and at other sacred rites.</i></p>
 <p align="justify" style="line-height: 100%; margin-bottom: 0in"><i>	Our new Hymnal
 has been compiled by a Committee of priests and laymen. The Committee
 aimed at including the widest possible selection of Catholic hymns
 from a variety of sources. Special attention was also paid to the
 need to assist those who lead singing in our assemblies by providing
 hymns appropriate to the different parts of the Mass. Many old
 favorite hymns have been included, hymns loved and cherished by all
 Cameroonians; but care has also been taken to include many new hymns
 which are worth learning.</i></p>
 <p align="justify" style="line-height: 100%; margin-bottom: 0in"><i>	It is my hope
 that this collection of hymns will provide an ample and stimulating
 choice for all tastes and for every occasion. I hope, furthermore,
 that this hymnal will halt the decline in congregational singing, and
 promote greater liturgical unity within the Bamenda Ecclesiastical
 Province.</i></p>
 <p align="justify" style="line-height: 100%; margin-bottom: 0in"><i>	We all owe a
 debt of gratitude to the members of the Committee which did a very
 good job in compiling the new hymnal, taking special care that all
 the texts chosen should be in conformity with Catholic Doctrine.</i></p>
 <p align="justify" style="line-height: 100%; margin-bottom: 0in"><br/>

 </p>
 <p align="justify" style="font-variant: small-caps; line-height: 100%; margin-bottom: 0in">
